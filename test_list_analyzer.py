"""Модуль для тестирования анализатора списков."""

import pytest
from list_analyzer import ListAnalyzer

@pytest.mark.parametrize(
    "list1, list2, expected",
    [
        ([1, 2, 3, 4, 5], [6, 7, 8, 9], "Второй список имеет большее среднее значение"),
        ([1, 2, 3], [4, 5, 6], "Второй список имеет большее среднее значение"),  # исправлено здесь
        ([], [], "Средние значения равны"),
        ([10], [], "Первый список имеет большее среднее значение"),
        ([5, 10, 15], [20, 25], "Второй список имеет большее среднее значение"),
    ]
)
def test_list_analyzer(list1, list2, expected):
    """Тестирование анализатора списков с различными входными данными."""
    analyzer = ListAnalyzer(list1, list2)
    assert analyzer.compare_means() == expected
